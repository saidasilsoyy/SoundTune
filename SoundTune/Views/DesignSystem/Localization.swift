    func translate(_ key: String) -> String {
        let languageCode = currentLanguage.resolvedLanguage.rawValue
        if let translated = translations[key]?[languageCode] ?? supplementalTranslations[key]?[languageCode] {
            return translated
        }
        return translations[key]?[AppLanguage.english.rawValue] ?? key
    }

    func formatPercentage(_ fraction: Double, maximumFractionDigits: Int = 2) -> String {
        let formatter = NumberFormatter()
        formatter.numberStyle = .percent
        formatter.locale = currentLanguage.locale
        formatter.minimumFractionDigits = 0
        formatter.maximumFractionDigits = maximumFractionDigits
        return formatter.string(from: NSNumber(value: fraction)) ?? "\(fraction * 100)%"
    }
