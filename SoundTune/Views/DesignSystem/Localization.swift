import Foundation
import Observation

enum AppLanguage: String, Codable, CaseIterable, Identifiable {
    case system
    case english = "en"
    case turkish = "tr"
    case german = "de"
    case spanish = "es"
    case french = "fr"
    
    var id: String { rawValue }
    
    var displayName: String {
        switch self {
        case .system: return "System"
        case .english: return "English"
        case .turkish: return "Türkçe"
        case .german: return "Deutsch"
        case .spanish: return "Español"
        case .french: return "Français"
        }
    }

    var resolvedLanguage: AppLanguage {
        guard self == .system else { return self }
        return Self.resolve(preferredLanguages: Locale.preferredLanguages)
    }

    var locale: Locale {
        switch resolvedLanguage {
        case .turkish:
            return Locale(identifier: "tr_TR")
        case .german:
            return Locale(identifier: "de_DE")
        case .spanish:
            return Locale(identifier: "es_ES")
        case .french:
            return Locale(identifier: "fr_FR")
        case .system, .english:
            return Locale(identifier: "en_US")
        }
    }

    static func resolve(languageCode: String?) -> AppLanguage {
        supportedLanguage(for: languageCode) ?? .english
    }

    static func resolve(preferredLanguages: [String]) -> AppLanguage {
        for identifier in preferredLanguages {
            if let language = supportedLanguage(for: normalizedLanguageCode(from: identifier)) {
                return language
            }
        }
        return .english
    }

    private static func supportedLanguage(for languageCode: String?) -> AppLanguage? {
        guard let languageCode = normalizedLanguageCode(from: languageCode) else { return nil }
        return allCases.first { language in
            language != .system && language.rawValue == languageCode
        }
    }

    private static func normalizedLanguageCode(from identifier: String?) -> String? {
        guard let identifier, !identifier.isEmpty else { return nil }
        return identifier.replacingOccurrences(of: "_", with: "-")
            .split(separator: "-")
            .first
            .map { String($0).lowercased() }
    }
}

